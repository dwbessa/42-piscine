int	ft_sqrt(int nb)
{
	long	i;
	long	b;

	b = nb;
	if (b <= 0)
	{
		return (0);
	}
	if (b == 1)
	{
		return (1);
	}
	i = 2;
	if (b >= 2)
	{
		while ((i * i) <= b)
		{
			if ((i * i) == b)
			{
				return (i);
			}
			i++;
		}
	}
	return (0);
}
